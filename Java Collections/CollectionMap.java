import java.util.*;

// Collection Maps contain key-value pairs that require unique keys. 


public class CollectionMap {
    public static void main(String[] args) {

        Map<Integer, String> myMap = new HashMap<>();
 
        myMap.put(48076, "Southfield");
        myMap.put(85206, "Mesa");
        myMap.put(90210, "Beverly Hills");
        myMap.put(86301, "Prescott");
        
        System.out.println(myMap);
        
        
    }
}